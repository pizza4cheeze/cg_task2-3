package ru.vsu.cs.Grushevskaya;

public class ScreenPoint {
    int x;
    int y;

    public ScreenPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
